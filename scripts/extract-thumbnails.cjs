/**
 * extract-thumbnails.js
 *
 * Reads the DEVICE_THUMBNAILS object from _original.html,
 * decodes every base64 data-URI entry, and writes individual
 * image files into public/thumbnails/.
 *
 * Also prints a summary so we can verify the results.
 */

const fs = require('fs');
const path = require('path');

// ── paths ──────────────────────────────────────────────────
const ROOT = path.resolve(__dirname, '..');
const HTML_FILE = path.join(ROOT, '_original.html');
const OUT_DIR = path.join(ROOT, 'public', 'thumbnails');

// make sure output directory exists
fs.mkdirSync(OUT_DIR, { recursive: true });

// ── read source ────────────────────────────────────────────
const html = fs.readFileSync(HTML_FILE, 'utf-8');

// Locate the DEVICE_THUMBNAILS block.
// It starts with "const DEVICE_THUMBNAILS = {" and ends with the
// matching "};" — we grab everything in between.
const startMarker = 'const DEVICE_THUMBNAILS = {';
const startIdx = html.indexOf(startMarker);
if (startIdx === -1) {
  console.error('ERROR: Could not find DEVICE_THUMBNAILS in the HTML file.');
  process.exit(1);
}

// Find the matching closing "};".
// We'll search from the opening brace onward.
const braceStart = startIdx + startMarker.length - 1; // index of '{'
let depth = 0;
let endIdx = -1;
for (let i = braceStart; i < html.length; i++) {
  if (html[i] === '{') depth++;
  if (html[i] === '}') {
    depth--;
    if (depth === 0) {
      endIdx = i;
      break;
    }
  }
}

if (endIdx === -1) {
  console.error('ERROR: Could not find closing brace of DEVICE_THUMBNAILS.');
  process.exit(1);
}

const block = html.substring(braceStart, endIdx + 1); // the full { ... }

// ── parse entries ──────────────────────────────────────────
// Each entry looks like:
//   'some_key':'data:image/jpeg;base64,/9j/4AAQ...',
//   or
//   'some_key':'data:image/png;base64,iVBOR...',
//
// We use a regex that captures the key and the data-URI.
const entryRe = /'([a-zA-Z0-9_]+)'\s*:\s*'(data:image\/(jpeg|png);base64,([A-Za-z0-9+/=\r\n]+))'/g;

let match;
let count = 0;
const keys = [];

while ((match = entryRe.exec(block)) !== null) {
  const key = match[1];
  const mimeType = match[3]; // "jpeg" or "png"
  const base64Data = match[4];

  const ext = mimeType === 'png' ? '.png' : '.jpg';
  const filename = key + ext;
  const filePath = path.join(OUT_DIR, filename);

  const buffer = Buffer.from(base64Data, 'base64');
  fs.writeFileSync(filePath, buffer);

  keys.push(key);
  count++;
  console.log(`  [${count}] ${filename}  (${buffer.length} bytes)`);
}

console.log(`\nDone! Extracted ${count} thumbnail images to ${OUT_DIR}`);

// ── generate the mapping module ────────────────────────────
// We also write src/data/device-thumbnails.js so the app can
// reference the images by URL path.
const dataDir = path.join(ROOT, 'src', 'data');
fs.mkdirSync(dataDir, { recursive: true });

const lines = keys.map((key) => {
  // Determine extension that was actually written
  // Re-check mime from the block for this key
  const uriRe = new RegExp(`'${key}'\\s*:\\s*'data:image/(jpeg|png);base64,`);
  const m = block.match(uriRe);
  const ext = m && m[1] === 'png' ? '.png' : '.jpg';
  return `  '${key}': '/thumbnails/${key}${ext}',`;
});

const moduleContent = [
  '// Auto-generated by scripts/extract-thumbnails.js',
  '// Maps device keys to their thumbnail URL paths.',
  '',
  'export const DEVICE_THUMBNAILS = {',
  ...lines,
  '};',
  '',
].join('\n');

const moduleFile = path.join(dataDir, 'device-thumbnails.js');
fs.writeFileSync(moduleFile, moduleContent, 'utf-8');
console.log(`\nWrote mapping module to ${moduleFile}`);
